
local black_fmt = vim.api.nvim_create_augroup('black_fmt', {clear = true})

-- Allows certain file types to be closed with q; grabbed from LazyVim
vim.api.nvim_create_autocmd("FileType", {
	pattern = {
		"qf",
		"help",
		"man",
		"notify",
		"lspinfo",
		"spectre_panel",
		"startuptime",
		"tsplayground",
	},
	callback = function(event)
		vim.bo[event.buf].buflisted = false
		vim.keymap.set("n", "q", "<cmd>close<cr>", { buffer = event.buf, silent = true})
	end,
})

